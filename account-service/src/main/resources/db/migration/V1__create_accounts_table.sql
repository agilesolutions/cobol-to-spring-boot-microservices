--       RECORD CONTAINS 500 CHARACTERS.
--   01  ACCOUNT-RECORD.
--     05 ACCT-ID              PIC X(11).
--     05 ACCT-ENTITY-CD       PIC X(25).
--     05 ACCT-TYPE-CD         PIC X(1).
--     05 ACCT-ACTIVE-STATUS   PIC X(1).
--     05 ACCT-CURR-BAL        PIC S9(10)V99 COMP-3.
--     05 ACCT-CREDIT-LIMIT    PIC S9(10)V99 COMP-3.
--     ... etc
